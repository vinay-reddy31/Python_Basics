def sum(n):
    sum=0
    for i in n:
        sum+=i
    print(sum)

def maximum(n):
    max=n[0]
    for i in n:
        if(i>max):
            max=i
    print(max)

def minimum(n):
    min=n[0]
    for i in n:
        if(i<min):
            min=i
    print(min)





